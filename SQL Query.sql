CREATE EXTENSION IF NOT EXISTS tablefunc;

-- 1: Выплаты следующей недели по дням
WITH loan_totals AS (
    SELECT к.Фамилия || ' ' || к.Имя AS Клиент,
           ROUND(SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов + п.Остаток_погашения_штрафа), 2) AS Общая_задолженность,
           ROUND(SUM(CASE WHEN п.Статус_исполнения = 'Оплачено' THEN п.Остаток_погашения_основы + п.Остаток_погашения_процентов ELSE 0 END), 2) AS Погашено,
           ROUND(SUM(CASE WHEN п.Плановая_дата_оплаты BETWEEN date_trunc('week', CURRENT_DATE)::date + 7 AND date_trunc('week', CURRENT_DATE)::date + 13 THEN п.Остаток_погашения_основы + п.Остаток_погашения_процентов ELSE 0 END), 2) AS Выплатить_за_неделю
    FROM План_выплат п
    JOIN Документ_займа з ON з.id_Займа = п.id_Займа
    JOIN Клиент к ON к.id_Клиента = з.id_Клиента
    WHERE EXISTS (
        SELECT 1 FROM План_выплат пw WHERE пw.id_Займа = п.id_Займа AND пw.Плановая_дата_оплаты BETWEEN date_trunc('week', CURRENT_DATE)::date + 7 AND date_trunc('week', CURRENT_DATE)::date + 13
    )
    GROUP BY к.Фамилия, к.Имя
),
pivot_data AS (
    SELECT * FROM crosstab(
        $$ WITH wd AS (SELECT date_trunc('week', CURRENT_DATE)::date + 7 AS ws)
           SELECT к.Фамилия || ' ' || к.Имя,
                  CASE EXTRACT(ISODOW FROM п.Плановая_дата_оплаты)
                      WHEN 1 THEN '1_Пн' WHEN 2 THEN '2_Вт' WHEN 3 THEN '3_Ср' WHEN 4 THEN '4_Чт' WHEN 5 THEN '5_Пт' WHEN 6 THEN '6_Сб' WHEN 7 THEN '7_Вс' END,
                  ROUND(SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов), 2)
           FROM План_выплат п JOIN Документ_займа з ON з.id_Займа = п.id_Займа JOIN Клиент к ON к.id_Клиента = з.id_Клиента CROSS JOIN wd
           WHERE п.Плановая_дата_оплаты BETWEEN wd.ws AND wd.ws + 6 GROUP BY к.Фамилия, к.Имя, п.Плановая_дата_оплаты ORDER BY 1, 2 $$,
        $$ VALUES ('1_Пн'), ('2_Вт'), ('3_Ср'), ('4_Чт'), ('5_Пт'), ('6_Сб'), ('7_Вс') $$
    ) AS ct (Клиент TEXT, Пн NUMERIC, Вт NUMERIC, Ср NUMERIC, Чт NUMERIC, Пт NUMERIC, Сб NUMERIC, Вс NUMERIC)
)
SELECT lt.Клиент, lt.Общая_задолженность, lt.Погашено, lt.Выплатить_за_неделю, pd.Пн, pd.Вт, pd.Ср, pd.Чт, pd.Пт, pd.Сб, pd.Вс
FROM loan_totals lt JOIN pivot_data pd USING (Клиент) ORDER BY lt.Клиент;


-- 2: Клиенты, пропустившие выплату по плану
WITH план_с_количеством AS (
    SELECT к.Фамилия || ' ' || к.Имя AS Клиент, п.id_Плана, п.Плановая_дата_оплаты, п.Статус_исполнения,
           (CURRENT_DATE - п.Плановая_дата_оплаты) AS Дней_просрочки,
           COUNT(*) OVER (PARTITION BY п.id_Займа ORDER BY п.Плановая_дата_оплаты ROWS BETWEEN CURRENT ROW AND UNBOUNDED FOLLOWING) - 1 AS Пунктов_после,
           ROUND((п.Остаток_погашения_основы + п.Остаток_погашения_процентов) * з.Пени_процент_день * (CURRENT_DATE - п.Плановая_дата_оплаты) / 100.0, 2) AS Штраф
    FROM План_выплат п JOIN Документ_займа з ON з.id_Займа = п.id_Займа JOIN Клиент к ON к.id_Клиента = з.id_Клиента
)
SELECT Клиент, id_Плана, Плановая_дата_оплаты, Дней_просрочки, Пунктов_после, Штраф
FROM план_с_количеством WHERE Статус_исполнения = 'Просрочено' ORDER BY Клиент, Плановая_дата_оплаты;


-- 3: Количество займов по месяцам за текущий и предыдущий год
WITH по_месяцам AS (
    SELECT EXTRACT(YEAR FROM Дата_выдачи)::int AS Год, EXTRACT(MONTH FROM Дата_выдачи)::int AS Месяц,
           COUNT(*) AS Кол_договоров, SUM(Сумма_займа) AS Общая_сумма, COUNT(DISTINCT id_Клиента) AS Уник_клиентов, id_Бухгалтера
    FROM Документ_займа WHERE EXTRACT(YEAR FROM Дата_выдачи) IN (EXTRACT(YEAR FROM CURRENT_DATE), EXTRACT(YEAR FROM CURRENT_DATE) - 1) GROUP BY 1, 2, id_Бухгалтера
),
с_рангом AS (SELECT *, RANK() OVER (PARTITION BY Год, Месяц ORDER BY Кол_договоров DESC) AS rnk FROM по_месяцам),
суммы AS (SELECT Год, Месяц, SUM(Кол_договоров) AS Всего_договоров, SUM(Общая_сумма) AS Общая_сумма, SUM(Уник_клиентов) AS Уник_клиентов FROM по_месяцам GROUP BY Год, Месяц),
лучший AS (SELECT DISTINCT ON (Год, Месяц) Год, Месяц, id_Бухгалтера, Кол_договоров AS Кол_у_лучшего FROM с_рангом WHERE rnk = 1 ORDER BY Год, Месяц)
SELECT с.Год, с.Месяц, с.Всего_договоров, ROUND(с.Общая_сумма, 2) AS Общая_сумма, с.Уник_клиентов, со.Фамилия || ' ' || со.Имя AS Лучший_сотрудник, л.Кол_у_лучшего
FROM суммы с JOIN лучший л USING (Год, Месяц) JOIN Сотрудник со ON со.id_Сотрудника = л.id_Бухгалтера ORDER BY с.Год, с.Месяц;


-- 4: Обслуживание клиентов в отделениях
WITH последний_займ AS (
    SELECT DISTINCT ON (з.id_Клиента) з.id_Клиента, з.Дата_выдачи AS Дата_последнего, з.Сумма_займа AS Сумма_последнего, то_.Название || ' (' || о.Номер_лицензии || ')' AS Отделение_последнего
    FROM Документ_займа з JOIN Отделение о ON о.id_Отделения = з.id_Отделения JOIN Тип_отделения то_ ON то_.id_Типа = о.id_Типа ORDER BY з.id_Клиента, з.Дата_выдачи DESC
),
интервалы AS (
    SELECT id_Клиента, AVG(diff_days) AS Ср_дней_между
    FROM (SELECT id_Клиента, (Дата_выдачи - LAG(Дата_выдачи) OVER (PARTITION BY id_Клиента ORDER BY Дата_выдачи))::int AS diff_days FROM Документ_займа) d WHERE diff_days IS NOT NULL GROUP BY id_Клиента
),
агрегат AS (
    SELECT з.id_Клиента, к.Фамилия || ' ' || к.Имя AS ФИО, STRING_AGG(DISTINCT то_.Название || ' (' || о.Номер_лицензии || ')', ', ' ORDER BY то_.Название || ' (' || о.Номер_лицензии || ')') AS Отделения, COUNT(*) AS Кол_займов, COUNT(DISTINCT з.id_Отделения) AS Кол_отделений
    FROM Документ_займа з JOIN Клиент к ON к.id_Клиента = з.id_Клиента JOIN Отделение о ON о.id_Отделения = з.id_Отделения JOIN Тип_отделения то_ ON то_.id_Типа = о.id_Типа GROUP BY з.id_Клиента, к.Фамилия, к.Имя
)
SELECT аг.ФИО, аг.Отделения, аг.Кол_займов, ROUND(аг.Кол_займов::numeric / NULLIF(аг.Кол_отделений, 0), 2) AS Займов_на_отделение,
       пз.Дата_последнего, пз.Сумма_последнего, пз.Отделение_последнего AS Последнее_отделение, CASE WHEN ин.Ср_дней_между < 30 THEN 'Да' ELSE 'Нет' END AS Чаще_раза_в_месяц
FROM агрегат аг JOIN последний_займ пз USING (id_Клиента) LEFT JOIN интервалы ин USING (id_Клиента) WHERE аг.Кол_займов > 1 ORDER BY аг.Кол_займов DESC;


-- 5: Коллекторские агентства
WITH агентства AS (
    SELECT кa.id_Агентства, кa.Полное_наименование AS Агентство, COUNT(д.id_Договора) AS Кол_договоров, COUNT(DISTINCT з.id_Клиента) AS Уник_должников,
           ROUND(SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов + п.Остаток_погашения_штрафа), 2) AS Общая_сумма, MAX(д.Дата_подписания) AS Дата_последнего
    FROM Договор_о_сотрудничестве д JOIN Документ_займа з ON з.id_Займа = д.id_Займа JOIN Коллекторское_агентство кa ON кa.id_Агентства = д.id_Агентства LEFT JOIN План_выплат п ON п.id_Займа = з.id_Займа GROUP BY кa.id_Агентства, кa.Полное_наименование
),
задолженность_по_договору AS (
    SELECT д.id_Агентства, д.Дата_подписания, SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов + п.Остаток_погашения_штрафа) AS Задолж_по_договору
    FROM Договор_о_сотрудничестве д JOIN Документ_займа з ON з.id_Займа = д.id_Займа LEFT JOIN План_выплат п ON п.id_Займа = з.id_Займа GROUP BY д.id_Агентства, д.id_Договора, д.Дата_подписания
),
макс_задолж AS (
    SELECT DISTINCT ON (id_Агентства) id_Агентства, ROUND(Задолж_по_договору, 2) AS Макс_задолж, Дата_подписания AS Дата_макс
    FROM задолженность_по_договору ORDER BY id_Агентства, Задолж_по_договору DESC
)
SELECT а.Агентство, а.Кол_договоров, а.Уник_должников, а.Общая_сумма, а.Дата_последнего, мз.Макс_задолж AS Крупнейшая_задолж, мз.Дата_макс AS Дата_крупнейшей
FROM агентства а JOIN макс_задолж мз USING (id_Агентства) ORDER BY а.Кол_договоров DESC;