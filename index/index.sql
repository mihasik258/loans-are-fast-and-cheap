
-- запрос 2: клиенты с просроченными выплатами
EXPLAIN (ANALYZE, BUFFERS)
SELECT Клиент, id_Плана, Плановая_дата_оплаты, Дней_просрочки, Пунктов_после, Штраф
FROM (
    SELECT к.Фамилия || ' ' || к.Имя AS Клиент, п.id_Плана, п.Плановая_дата_оплаты, п.Статус_исполнения,
        (CURRENT_DATE - п.Плановая_дата_оплаты) AS Дней_просрочки,
        COUNT(*) OVER (PARTITION BY п.id_Займа ORDER BY п.Плановая_дата_оплаты ROWS BETWEEN CURRENT ROW AND UNBOUNDED FOLLOWING) - 1 AS Пунктов_после, -- ROWS BETWEEN считает количество оставшихся выплат после просрочки.
        ROUND((п.Остаток_погашения_основы + п.Остаток_погашения_процентов) * з.Пени_процент_день * (CURRENT_DATE - п.Плановая_дата_оплаты) / 100.0, 2) AS Штраф
    FROM План_выплат п
    JOIN Документ_займа з ON п.id_Займа = з.id_Займа
    JOIN Клиент к ON з.id_Клиента = к.id_Клиента
) t
WHERE Статус_исполнения = 'Просрочено'
ORDER BY Клиент, Плановая_дата_оплаты;

-- запрос 4: обслуживание клиентов в отделениях
EXPLAIN (ANALYZE, BUFFERS)
SELECT аг.ФИО, аг.Отделения, аг.Кол_займов, аг.Займов_на_отделение, з.Дата_выдачи AS Дата_последнего, з.Сумма_займа AS Сумма_последнего, о.Номер_лицензии AS Отделение_последнего, аг.Чаще_раза_в_месяц
FROM Документ_займа з
JOIN Клиент к ON з.id_Клиента = к.id_Клиента
JOIN Отделение о ON з.id_Отделения = о.id_Отделения
LEFT JOIN Документ_займа з2 ON з2.id_Клиента = з.id_Клиента AND з2.Дата_выдачи > з.Дата_выдачи
JOIN (
    SELECT з.id_Клиента, к.Фамилия || ' ' || к.Имя AS ФИО, STRING_AGG(DISTINCT о.Номер_лицензии, ', ') AS Отделения, COUNT(*) AS Кол_займов, ROUND(CAST(COUNT(*) AS NUMERIC) / COUNT(DISTINCT з.id_Отделения), 2) AS Займов_на_отделение,
        CASE WHEN AVG(интервал) < 30 THEN 'Да' ELSE 'Нет' END AS Чаще_раза_в_месяц -- список всех отделений
    FROM (
        SELECT з.*, CAST((з.Дата_выдачи - LAG(з.Дата_выдачи) OVER (PARTITION BY з.id_Клиента ORDER BY з.Дата_выдачи)) AS INTEGER) AS интервал
        FROM Документ_займа з
    ) з
    JOIN Клиент к ON з.id_Клиента = к.id_Клиента
    JOIN Отделение о ON з.id_Отделения = о.id_Отделения
    GROUP BY з.id_Клиента, к.Фамилия, к.Имя
    HAVING COUNT(*) > 1
) аг ON аг.id_Клиента = з.id_Клиента
WHERE з2.id_Займа IS NULL -- Анти-join
ORDER BY аг.Кол_займов DESC;


-- 1. СОСТАВНОЙ индекс на План_выплат
--    Порядок колонок: ключ JOIN → ключ сортировки оконной функции → поле фильтра.
--    Ускоряет сортировку внутри WindowAgg и даёт возможность фильтрации по статусу.
CREATE INDEX IF NOT EXISTS idx_план_займ_дата_статус
    ON "План_выплат" ("id_Займа", "Плановая_дата_оплаты", "Статус_исполнения");

-- 2. ЧАСТИЧНЫЙ индекс на План_выплат
--    Физически содержит только просроченные строки (~52 строки вместо 1458).
--    Эффективен при рефакторинге запроса с переносом фильтра внутрь подзапроса.
CREATE INDEX IF NOT EXISTS idx_план_просрочено
    ON "План_выплат" ("id_Займа", "Плановая_дата_оплаты")
    WHERE "Статус_исполнения" = 'Просрочено';

-- 3. ПОКРЫВАЮЩИЙ индекс на Документ_займа
--    Поддерживает три прохода по таблице в Запросе 4:
--      - LAG() с PARTITION BY id_Клиента ORDER BY Дата_выдачи
--      - anti-join з2.Дата_выдачи > з.Дата_выдачи
--      - SELECT з.Сумма_займа, з.id_Отделения
--    INCLUDE = данные для SELECT без обращения к heap (Index Only Scan).
CREATE INDEX IF NOT EXISTS idx_заём_клиент_дата_covering
    ON "Документ_займа" ("id_Клиента", "Дата_выдачи")
    INCLUDE ("id_Займа", "id_Отделения", "Сумма_займа");

