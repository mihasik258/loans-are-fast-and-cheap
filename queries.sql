-- 1: Сложный JOIN + HAVING
-- Топ отделений по объёму выданных займов за последние 3 года.
-- Только отделения с 2+ займами.
WITH активность_отделений AS (
    SELECT о.ID_Отделения,
        а.Город,
        то.Название AS Тип_отделения,
        COUNT(DISTINCT з.ID_Займа) AS Кол_займов,
        SUM(з.Сумма_займа) AS Общая_сумма,
        AVG(з.Процентная_ставка_день) AS Средняя_ставка,
        COUNT(DISTINCT з.ID_Клиента) AS Уникальных_клиентов
    FROM Отделение о
        JOIN Адрес а ON о.ID_Адреса = а.ID_Адреса
        JOIN Тип_отделения то ON о.ID_Типа = то.ID_Типа
        JOIN Документ_займа з ON з.ID_Отделения = о.ID_Отделения
    WHERE з.Дата_выдачи >= CURRENT_DATE - INTERVAL '3 years'
    GROUP BY о.ID_Отделения,
        а.Город,
        то.Название
    HAVING COUNT(DISTINCT з.ID_Займа) >= 2
)
SELECT Город,
    Тип_отделения,
    Кол_займов,
    Общая_сумма,
    ROUND(Средняя_ставка::numeric, 2) AS Ср_ставка_день,
    Уникальных_клиентов
FROM активность_отделений
ORDER BY Общая_сумма DESC;
-- 2: Поиск отсутствующих данных (LEFT JOIN + IS NULL)
-- Клиенты, оформившие анкету, но ни разу не получавшие заём.
WITH клиенты_без_займов AS (
    SELECT к.ID_Клиента,
        к.Фамилия || ' ' || к.Имя AS ФИО_клиента,
        к.Контактный_телефон,
        а.Дата_первого_обращения,
        кат.Название AS Категория
    FROM Клиент к
        JOIN Анкета_клиента а ON а.ID_Клиента = к.ID_Клиента
        JOIN Категория_клиента кат ON а.ID_Категории = кат.ID_Категории
        LEFT JOIN Документ_займа з ON з.ID_Клиента = к.ID_Клиента
    WHERE з.ID_Займа IS NULL
)
SELECT ФИО_клиента,
    Контактный_телефон,
    Категория,
    Дата_первого_обращения
FROM клиенты_без_займов
ORDER BY Дата_первого_обращения;
-- 3: Оконная функция с ROWS BETWEEN
-- Накопительный итог выплат по каждому займу (погашение основы
-- + процентов нарастающим итогом от 1-й до текущей записи плана).
-- Показывает % выплаченности на каждую дату.
WITH платежи_по_плану AS (
    SELECT п.ID_Плана,
        п.ID_Займа,
        п.Плановая_дата_оплаты,
        п.Остаток_погашения_основы AS Основа,
        п.Остаток_погашения_процентов AS Проценты,
        п.Статус_исполнения
    FROM План_выплат п
),
накопительный_итог AS (
    SELECT ID_Займа,
        ID_Плана,
        Плановая_дата_оплаты,
        Статус_исполнения,
        Основа,
        SUM(Основа + Проценты) OVER (
            PARTITION BY ID_Займа
            ORDER BY Плановая_дата_оплаты ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
        ) AS Накопленная_задолженность,
        SUM(Основа + Проценты) OVER (PARTITION BY ID_Займа) AS Всего_по_займу
    FROM платежи_по_плану
)
SELECT ID_Займа,
    Плановая_дата_оплаты,
    Статус_исполнения,
    ROUND(Основа, 2) AS Основа,
    ROUND(Накопленная_задолженность, 2) AS Накоплено,
    ROUND(Всего_по_займу, 2) AS Итого_по_займу,
    ROUND(
        Накопленная_задолженность / NULLIF(Всего_по_займу, 0) * 100,
        1
    ) AS Процент_выплачено
FROM накопительный_итог
WHERE ID_Займа IN (
        SELECT ID_Займа
        FROM накопительный_итог
        GROUP BY ID_Займа
        HAVING COUNT(*) > 1
    )
ORDER BY ID_Займа,
    Плановая_дата_оплаты;
-- 4: Оконная функция смещения (LAG / LEAD)
-- История платежей клиента: предыдущий и следующий платёж,
-- интервал в днях между внесениями.
WITH платежи_клиента AS (
    SELECT к.ID_Клиента,
        к.Фамилия || ' ' || к.Имя AS ФИО,
        пд.Дата_время_платежа::date AS Дата_платежа,
        пд.Внесенная_сумма,
        пд.Назначение_платежа
    FROM Платёжный_документ пд
        JOIN Клиент к ON к.ID_Клиента = пд.ID_Клиента
),
с_лагом AS (
    SELECT ID_Клиента,
        ФИО,
        Дата_платежа,
        Внесенная_сумма,
        Назначение_платежа,
        LAG(Внесенная_сумма) OVER (
            PARTITION BY ID_Клиента
            ORDER BY Дата_платежа
        ) AS Предыдущий_платеж,
        LEAD(Внесенная_сумма) OVER (
            PARTITION BY ID_Клиента
            ORDER BY Дата_платежа
        ) AS Следующий_платеж,
        LAG(Дата_платежа) OVER (
            PARTITION BY ID_Клиента
            ORDER BY Дата_платежа
        ) AS Дата_предыдущего
    FROM платежи_клиента
)
SELECT ФИО,
    Дата_платежа,
    Внесенная_сумма,
    Предыдущий_платеж,
    Следующий_платеж,
    (Дата_платежа - Дата_предыдущего) AS Дней_между_платежами,
    Назначение_платежа
FROM с_лагом
WHERE ID_Клиента IN (
        SELECT ID_Клиента
        FROM Платёжный_документ
        GROUP BY ID_Клиента
        HAVING COUNT(*) >= 3
    )
ORDER BY ID_Клиента,
    Дата_платежа;
-- 5: Рейтинг бухгалтеров по кредитному портфелю
-- Ранжирование сотрудников-бухгалтеров по объёму
-- обслуживаемого ими портфеля займов с показателями качества.
WITH сотрудник_нагрузка AS (
    SELECT с.ID_Сотрудника,
        с.Фамилия || ' ' || с.Имя AS ФИО_Бухгалтера,
        д.Название AS Должность,
        а.Город,
        COUNT(DISTINCT з.ID_Займа) AS Кол_займов,
        COUNT(DISTINCT п.ID_Плана) AS Кол_строк_плана,
        SUM(з.Сумма_займа) AS Общий_портфель,
        COUNT(
            DISTINCT CASE
                WHEN з.Текущий_статус = 'Просрочен' THEN з.ID_Займа
            END
        ) AS Просроченных,
        COUNT(
            DISTINCT CASE
                WHEN з.Текущий_статус = 'Передан' THEN з.ID_Займа
            END
        ) AS Передано_коллекторам
    FROM Сотрудник с
        JOIN Должность д ON с.ID_Должности = д.ID_Должности
        JOIN Отделение о ON с.ID_Отделения = о.ID_Отделения
        JOIN Адрес а ON о.ID_Адреса = а.ID_Адреса
        LEFT JOIN Документ_займа з ON з.ID_Бухгалтера = с.ID_Сотрудника
        LEFT JOIN План_выплат п ON п.ID_Бухгалтера = с.ID_Сотрудника
    WHERE д.Название = 'Бухгалтер'
    GROUP BY с.ID_Сотрудника,
        с.Фамилия,
        с.Имя,
        д.Название,
        о.ID_Отделения,
        а.Город
    HAVING COUNT(DISTINCT з.ID_Займа) > 0
),
ранжирование AS (
    SELECT *,
        RANK() OVER (
            ORDER BY Общий_портфель DESC
        ) AS Ранг_по_портфелю,
        ROUND(
            Просроченных::numeric / NULLIF(Кол_займов, 0) * 100,
            1
        ) AS Процент_просроченных
    FROM сотрудник_нагрузка
)
SELECT Ранг_по_портфелю,
    ФИО_Бухгалтера,
    Город,
    Кол_займов,
    Кол_строк_плана,
    ROUND(Общий_портфель) AS Портфель_руб,
    Просроченных,
    Передано_коллекторам,
    Процент_просроченных
FROM ранжирование
ORDER BY Ранг_по_портфелю;