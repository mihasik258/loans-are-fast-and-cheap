        Статус_исполнения,
        ID_Займа,
        ID_Бухгалтера,
        ID_Типа_начисления
    )
SELECT CURRENT_DATE - 5,
    1000, 100, 0, 'Просрочено'::статус_исполнения_плана_enum,
    (
        SELECT MIN(id_Займа)
        FROM Документ_займа
        WHERE Текущий_статус = 'Активен'
    ),
    (
        SELECT MIN(id_Сотрудника)
        FROM Сотрудник
    ),
    (
        SELECT MIN(id_Типа_начисления)
        FROM Тип_начисления
    );
COMMIT;

-- СЕССИЯ 1

-- Шаг 2.8: Второй подсчёт на REPEATABLE READ
SELECT COUNT(*) AS просрочено
FROM План_выплат
WHERE Статус_исполнения = 'Просрочено';
-- результат: счётчик НЕ изменился — снимок данных заморожен.
-- Фантомная строка не видна.
ROLLBACK;

DELETE FROM План_выплат
WHERE Плановая_дата_оплаты = CURRENT_DATE - 5
    AND Остаток_погашения_основы = 1000
    AND Статус_исполнения = 'Просрочено';
    
-- write skew;
-- две транзакции читают одно и то же условие,
-- каждая решает «можно записать», обе фиксируют данные —
-- в итоге инвариант нарушен (ни одна транзакция не
-- нарушила его в одиночку, но обе вместе — нарушили).
-- Бизнес-кейс: две кассы выдают займы одному клиенту;
-- каждая проверяет лимит 2 млн и видит его соблюдение,
-- но обе вместе превышают его.

-- смотрим текущий долг клиента ID=1
SELECT id_Клиента,
    SUM(
        п.Остаток_погашения_основы + п.Остаток_погашения_процентов + COALESCE(п.Остаток_погашения_штрафа, 0)
    ) AS текущий_долг
FROM План_выплат п
    JOIN Документ_займа з ON п.id_Займа = з.id_Займа
WHERE з.id_Клиента = 1
    AND п.id_Платежа IS NULL
GROUP BY id_Клиента;
-- СЕССИЯ 1
-- Шаг 3.1: Касса 1 открывает транзакцию
BEGIN ISOLATION LEVEL REPEATABLE READ;
-- Шаг 3.2: Проверяет долг — видим X рублей (< 2 000 000)
SELECT COALESCE(
        SUM(
            п.Остаток_погашения_основы + п.Остаток_погашения_процентов + COALESCE(п.Остаток_погашения_штрафа, 0)
        ),
        0
    ) AS текущий_долг
FROM План_выплат п
    JOIN Документ_займа з ON п.id_Займа = з.id_Займа
WHERE з.id_Клиента = 1
    AND п.id_Платежа IS NULL
    AND з.Текущий_статус NOT IN ('Закрыт', 'Передан');

-- СЕССИЯ 2 

-- Шаг 3.3: Касса 2 открывает транзакцию параллельно
BEGIN ISOLATION LEVEL REPEATABLE READ;
-- Шаг 3.4: Тоже читает тот же долг
SELECT COALESCE(
        SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов + COALESCE(п.Остаток_погашения_штрафа, 0)
        ), 0) AS текущий_долг
FROM План_выплат п
    JOIN Документ_займа з ON п.id_Займа = з.id_Займа
WHERE з.id_Клиента = 1
    AND п.id_Платежа IS NULL
    AND з.Текущий_статус NOT IN ('Закрыт', 'Передан');
    
-- Шаг 3.5: Касса 2 выдаёт займ (INSERT в Документ_займа)
INSERT INTO Документ_займа (
        Дата_выдачи,
        Сумма_займа,
        Тип_выплат,
        Количество_дней,
        Крайняя_дата_возврата,
        Процентная_ставка_день,
        Пени_процент_день,
        id_Клиента,
        id_Отделения,
        id_Бухгалтера
    )
SELECT CURRENT_DATE,
    900000, 'Единовременный', 30, CURRENT_DATE + 30, 1, 1, 1,
    (
        SELECT MIN(id_Отделения)
        FROM Отделение
    ),
    (
        SELECT MIN(id_Сотрудника)
        FROM Сотрудник
    );
COMMIT;
-- Касса 2 зафиксировала — клиент получил 900 000.

-- СЕССИЯ 1

-- Шаг 3.6: Касса 1 тоже выдаёт займ на 900 000
INSERT INTO Документ_займа (
        Дата_выдачи,
        Сумма_займа,
        Тип_выплат,
