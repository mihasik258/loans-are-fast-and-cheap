-- non-repeatable read
-- одна транзакция дважды читает одну строку
-- и получает разные значения, потому что другая транзакция
-- успела изменить и зафиксировать эту строку между чтениями.
-- фиксируем тестовую строку
SELECT id_Займа,
    Сумма_займа,
    Текущий_статус
FROM Документ_займа
WHERE id_Займа = 1;

-- СЕССИЯ 1

-- Шаг 1.1: Открываем транзакцию с уровнем READ COMMITTED
BEGIN ISOLATION LEVEL READ COMMITTED;
-- Шаг 1.2: Первое чтение — запоминаем сумму займа
SELECT id_Займа,
    Сумма_займа
FROM Документ_займа
WHERE id_Займа = 1;
-- Результат: видим исходную сумму

-- СЕССИЯ 2

-- Шаг 1.3: Другой пользователь изменяет сумму и фиксирует изменения
BEGIN;
UPDATE Документ_займа
SET Сумма_займа = 999999.00
WHERE id_Займа = 1;
COMMIT;
-- Изменение зафиксировано — данные в таблице изменены

-- СЕССИЯ 1

-- Шаг 1.4: Второе чтение той же строки в той же транзакции
SELECT id_Займа,
    Сумма_займа
FROM Документ_займа
WHERE id_Займа = 1;
-- Результат: видим 999 999 — данные изменились
-- non-repeatable read: два SELECT в одной транзакции
-- вернули разные значения.
ROLLBACK;
UPDATE Документ_займа
SET Сумма_займа = 500000.00
WHERE id_Займа = 1;

-- phantom read
-- транзакция дважды выбирает набор строк по условию,
-- но между чтениями другая транзакция вставила новые строки,
-- которые тоже попадают под это условие.
-- При REPEATABLE READ в PostgreSQL фантомы блокируются
-- снимком данных — демонстрируем разницу двух уровней.


-- смотрим количество просроченных пунктов
SELECT COUNT(*)
FROM План_выплат
WHERE Статус_исполнения = 'Просрочено';
-- READ COMMITTED — фантом виден

-- СЕССИЯ 1 

-- Шаг 2.1
BEGIN ISOLATION LEVEL READ COMMITTED;
-- Шаг 2.2: Первый подсчёт просроченных строк
SELECT COUNT(*) AS просрочено
FROM План_выплат
WHERE Статус_исполнения = 'Просрочено';

-- СЕССИЯ 2 

-- Шаг 2.3: Добавляем новый просроченный пункт и фиксируем
BEGIN;
INSERT INTO План_выплат (
        Плановая_дата_оплаты,
        Остаток_погашения_основы,
        Остаток_погашения_процентов,
        Остаток_погашения_штрафа,
        Статус_исполнения,
        ID_Займа,
        ID_Бухгалтера,
        ID_Типа_начисления
    )
SELECT CURRENT_DATE - 5,
    1000, 100, 0, 'Просрочено'::статус_исполнения_плана_enum,
    (
        SELECT MIN(id_Займа)
        FROM Документ_займа
        WHERE Текущий_статус = 'Активен'
    ),
    (
        SELECT MIN(id_Сотрудника)
        FROM Сотрудник
    ),
    (
        SELECT MIN(id_Типа_начисления)
        FROM Тип_начисления
    );
COMMIT;

-- СЕССИЯ 1

-- Шаг 2.4: Второй подсчёт — фантом виден на READ COMMITTED
SELECT COUNT(*) AS просрочено
FROM План_выплат
WHERE Статус_исполнения = 'Просрочено';
-- результат: счётчик увеличился на 1 — PHANTOM READ
ROLLBACK;

DELETE FROM План_выплат
WHERE Плановая_дата_оплаты = CURRENT_DATE - 5
    AND Остаток_погашения_основы = 1000
    AND Статус_исполнения = 'Просрочено';
-- REPEATABLE READ — фантом НЕ виден

-- СЕССИЯ 1

-- Шаг 2.5
BEGIN ISOLATION LEVEL REPEATABLE READ;
-- Шаг 2.6: Первый подсчёт
SELECT COUNT(*) AS просрочено
FROM План_выплат
WHERE Статус_исполнения = 'Просрочено';

-- СЕССИЯ 2

-- Шаг 2.7: Снова добавляем строку и фиксируем
BEGIN;
INSERT INTO План_выплат (
