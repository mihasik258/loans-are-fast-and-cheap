    );
COMMIT;
-- Касса 1 тоже зафиксировала
-- клиент получил 1 800 000 сверх уже имевшегося долга.
-- WRITE SKEW зафиксирован: инвариант нарушен, хотя каждая
-- транзакция в момент чтения видела корректные данные.
DELETE FROM Документ_займа
WHERE id_Клиента = 1
    AND Сумма_займа = 900000
    AND Дата_выдачи = CURRENT_DATE;
    
-- SAVEPOINT и ROLLBACK TO
-- Демонстрация частичного отката транзакции.
-- Бизнес-кейс: оператор обрабатывает пакет операций;
-- одна операция оказывается ошибочной, но откатывать
-- всё не нужно — откатываем только с последней точки.
BEGIN;
-- Шаг 4.1: Первое действие — помечаем займ как «Просрочен»
UPDATE Документ_займа
SET Текущий_статус = 'Просрочен'
WHERE id_Займа = 2;
-- Шаг 4.2: Точка сохранения после первого действия
SAVEPOINT sp_после_займа;
SELECT id_Займа,
    Текущий_статус
FROM Документ_займа
WHERE id_Займа IN (2, 3);
-- Шаг 4.3: Второе действие — обновляем статус ещё одного займа
UPDATE Документ_займа
SET Текущий_статус = 'Передан'
WHERE id_Займа = 3;
SELECT id_Займа,
    Текущий_статус
FROM Документ_займа
WHERE id_Займа IN (2, 3);
-- id=2 - Просрочен, id=3 - Передан
-- Шаг 4.4: Обнаружили ошибку — займ 3 нельзя передавать.
-- второй UPDATE отменяется
ROLLBACK TO SAVEPOINT sp_после_займа;
SELECT id_Займа,
    Текущий_статус
FROM Документ_займа
WHERE id_Займа IN (2, 3);
-- id=2 - Просрочен, id=3 - Активен
-- Шаг 4.5: Создаём ещё одну точку и вносим корректное изменение
SAVEPOINT sp_после_коррекции;
UPDATE Документ_займа
SET Текущий_статус = 'Активен' -- займ 2 тоже остаётся активным
WHERE id_Займа = 2;
SELECT id_Займа,
    Текущий_статус
FROM Документ_займа
WHERE id_Займа IN (2, 3);
-- Шаг 4.6: Фиксируем результат
COMMIT;

UPDATE Документ_займа
SET Текущий_статус = 'Активен'
WHERE id_Займа IN (2, 3);