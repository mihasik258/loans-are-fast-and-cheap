-- Проверяем созданные договоры с агентством
SELECT дс.Регистрационный_номер,
    дс.Дата_подписания,
    дс.Статус_договора,
    к.Фамилия,
    к.Имя,
    з.Сумма_займа
FROM Договор_о_сотрудничестве дс
    JOIN Документ_займа з ON дс.ID_Займа = з.ID_Займа
    JOIN Клиент к ON з.ID_Клиента = к.ID_Клиента
ORDER BY дс.ID_Договора DESC
LIMIT 5;

