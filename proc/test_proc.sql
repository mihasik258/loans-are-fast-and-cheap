-- пункты плана которые должны считаться просроченными
SELECT п.ID_Плана,
    п.ID_Займа,
    п.Плановая_дата_оплаты,
    CURRENT_DATE - п.Плановая_дата_оплаты AS дней_просрочки,
    п.Остаток_погашения_основы + п.Остаток_погашения_процентов AS сумма
FROM План_выплат п
    JOIN Документ_займа з ON з.ID_Займа = п.ID_Займа
WHERE п.Плановая_дата_оплаты < CURRENT_DATE
    AND п.Статус_исполнения = 'Ожидает'
    AND з.Текущий_статус = 'Активен'
ORDER BY п.Плановая_дата_оплаты
LIMIT 5;

-- Т2А: запускаем процедуру начисления штрафов
CALL proc_проверка_задолженностей();

-- созданные штрафные пункты плана
SELECT п.ID_Плана,
    п.ID_Займа,
    п.Плановая_дата_оплаты,
    п.Остаток_погашения_штрафа,
    п.Статус_исполнения,
    тн.Название AS тип
FROM План_выплат п
    JOIN Тип_начисления тн ON тн.ID_Типа_начисления = п.ID_Типа_начисления
WHERE тн.Название ILIKE '%штраф%'
    AND п.Статус_исполнения = 'Ожидает'
ORDER BY п.ID_Плана DESC
LIMIT 5;

SELECT COUNT(*) AS штрафных_пунктов
FROM План_выплат п
    JOIN Тип_начисления тн ON тн.ID_Типа_начисления = п.ID_Типа_начисления
WHERE тн.Название ILIKE '%штраф%'
    AND п.Статус_исполнения = 'Ожидает';

