-- просроченные займы в базе
SELECT з.ID_Займа,
    к.Фамилия,
    к.Имя,
    SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов) AS сумма_просрочки,
    MAX(CURRENT_DATE - п.Плановая_дата_оплаты) AS дней_просрочки
FROM План_выплат п
    JOIN Документ_займа з ON п.ID_Займа = з.ID_Займа
    JOIN Клиент к ON з.ID_Клиента = к.ID_Клиента
WHERE п.Статус_исполнения = 'Просрочено'
GROUP BY з.ID_Займа, к.Фамилия, к.Имя
ORDER BY сумма_просрочки DESC
LIMIT 5;


