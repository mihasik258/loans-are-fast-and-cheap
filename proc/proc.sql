-- 1
-- p_сумма_млн — порог суммы просрочки в млн руб.
-- p_мес       — порог давности просрочки в месяцах
CREATE OR REPLACE PROCEDURE proc_стимулирование_должников(
    p_сумма_млн DECIMAL,
    p_мес       INT
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_лимит      DECIMAL := p_сумма_млн * 1000000;
    v_агентство  INT;
    v_должник    RECORD;
    v_причина    TEXT;
    v_рег_номер  VARCHAR(50);
    v_существует INT;
BEGIN
    SELECT ID_Агентства INTO v_агентство
    FROM Договор_о_сотрудничестве
    GROUP BY ID_Агентства
    ORDER BY COUNT(*) DESC
    LIMIT 1;

    IF v_агентство IS NULL THEN
        RAISE NOTICE 'Нет ни одного коллекторского агентства.';
        RETURN;
    END IF;

    FOR v_должник IN
        SELECT з.ID_Клиента, к.Фамилия || ' ' || к.Имя AS ФИО, з.ID_Займа,
            SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов) AS сумма_просрочки,
            MAX(CURRENT_DATE - п.Плановая_дата_оплаты) AS дней_просрочки
        FROM План_выплат п
        JOIN Документ_займа з ON п.ID_Займа = з.ID_Займа
        JOIN Клиент к ON з.ID_Клиента = к.ID_Клиента
        WHERE п.Статус_исполнения = 'Просрочено'
        GROUP BY з.ID_Клиента, к.Фамилия, к.Имя, з.ID_Займа
        HAVING SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов) > v_лимит
            OR MAX(CURRENT_DATE - п.Плановая_дата_оплаты) > p_мес * 30
    LOOP
        SELECT COUNT(*) INTO v_существует
        FROM Договор_о_сотрудничестве
        WHERE ID_Займа = v_должник.ID_Займа AND Статус_договора = 'В работе';

        IF v_существует > 0 THEN
            CONTINUE;
        END IF;

        v_причина := '';
        IF v_должник.сумма_просрочки > v_лимит THEN
            v_причина := 'сумма просрочки ' || v_должник.сумма_просрочки || ' руб.';
        END IF;
        IF v_должник.дней_просрочки > p_мес * 30 THEN
            IF v_причина <> '' THEN v_причина := v_причина || '; '; END IF;
            v_причина := v_причина || 'просрочка ' || v_должник.дней_просрочки || ' дней';
        END IF;

        v_рег_номер := 'КД-' || v_должник.ID_Займа || '-' || EXTRACT(EPOCH FROM CURRENT_TIMESTAMP)::BIGINT;

        INSERT INTO Договор_о_сотрудничестве
            (Регистрационный_номер, Дата_подписания, Статус_договора, ID_Займа, ID_Агентства, ID_Бухгалтера)
        VALUES (v_рег_номер, CURRENT_DATE, 'В работе', v_должник.ID_Займа, v_агентство,
            (SELECT ID_Бухгалтера FROM Документ_займа WHERE ID_Займа = v_должник.ID_Займа));

        RAISE NOTICE 'Договор создан: клиент %, займ %, причина: %',
            v_должник.ФИО, v_должник.ID_Займа, v_причина;
    END LOOP;
END;
$$;


-- 2
-- Для просрочки 1 день  — создаёт штрафной пункт плана.
-- Для просрочки > 1 дня — пересчитывает сумму штрафа.
CREATE OR REPLACE PROCEDURE proc_проверка_задолженностей()
LANGUAGE plpgsql
AS $$
DECLARE
    v_пункт      RECORD;
    v_просрочка  INT;
    v_штраф      DECIMAL(15,2);
    v_тип_штрафа INT;
BEGIN
    SELECT ID_Типа_начисления INTO v_тип_штрафа
    FROM Тип_начисления WHERE Название ILIKE '%штраф%' LIMIT 1;

    IF v_тип_штрафа IS NULL THEN
        RAISE EXCEPTION 'Не найден тип начисления "Штраф" в таблице Тип_начисления';
    END IF;

    FOR v_пункт IN
        SELECT п.*, з.Пени_процент_день, з.ID_Бухгалтера
        FROM План_выплат п
        JOIN Документ_займа з ON п.ID_Займа = з.ID_Займа
        WHERE п.Плановая_дата_оплаты < CURRENT_DATE
          AND п.Статус_исполнения = 'Ожидает'
          AND з.Текущий_статус = 'Активен'
    LOOP
        v_просрочка := CURRENT_DATE - v_пункт.Плановая_дата_оплаты;
        v_штраф := ROUND(
            (v_пункт.Остаток_погашения_основы + v_пункт.Остаток_погашения_процентов)
            * v_пункт.Пени_процент_день * v_просрочка / 100.0, 2);


        IF v_просрочка = 1 THEN
            UPDATE План_выплат SET Статус_исполнения = 'Просрочено' WHERE ID_Плана = v_пункт.ID_Плана;

            INSERT INTO План_выплат
                (Плановая_дата_оплаты, Остаток_погашения_основы, Остаток_погашения_процентов,
                 Остаток_погашения_штрафа, Статус_исполнения, ID_Займа, ID_Бухгалтера, ID_Типа_начисления)
            VALUES (CURRENT_DATE, 0, 0, v_штраф, 'Ожидает', v_пункт.ID_Займа, v_пункт.ID_Бухгалтера, v_тип_штрафа);

            RAISE NOTICE 'Займ %, план %: просрочка 1 день, штраф % руб.',
                v_пункт.ID_Займа, v_пункт.ID_Плана, v_штраф;

        ELSIF v_просрочка > 1 THEN
            UPDATE План_выплат SET Остаток_погашения_штрафа = v_штраф
            WHERE ID_Займа = v_пункт.ID_Займа AND ID_Типа_начисления = v_тип_штрафа
              AND Статус_исполнения = 'Ожидает' AND Плановая_дата_оплаты > v_пункт.Плановая_дата_оплаты;

            IF NOT FOUND THEN
                INSERT INTO План_выплат
                    (Плановая_дата_оплаты, Остаток_погашения_основы, Остаток_погашения_процентов,
                     Остаток_погашения_штрафа, Статус_исполнения, ID_Займа, ID_Бухгалтера, ID_Типа_начисления)
                VALUES (CURRENT_DATE, 0, 0, v_штраф, 'Ожидает', v_пункт.ID_Займа, v_пункт.ID_Бухгалтера, v_тип_штрафа);
            END IF;

            RAISE NOTICE 'Займ %, план %: просрочка % дней, пересчитан штраф % руб.',
                v_пункт.ID_Займа, v_пункт.ID_Плана, v_просрочка, v_штраф;
        END IF;
    END LOOP;
END;
$$;
