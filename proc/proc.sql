-- 1
-- p_сумма_млн — порог суммы просрочки в млн руб.
-- p_мес       — порог давности просрочки в месяцах
CREATE OR REPLACE PROCEDURE proc_стимулирование_должников(
    p_сумма_млн DECIMAL,
    p_мес       INT
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_лимит      DECIMAL := p_сумма_млн * 1000000;
    v_агентство  INT;
    v_должник    RECORD;
    v_причина    TEXT;
    v_рег_номер  VARCHAR(50);
    v_существует INT;
BEGIN
    SELECT дс.ID_Агентства INTO v_агентство
    FROM Договор_о_сотрудничестве дс
    JOIN Документ_займа дз ON дс.ID_Займа = дз.ID_Займа
    GROUP BY дс.ID_Агентства
    ORDER BY SUM(дз.Сумма_займа) DESC
    LIMIT 1;

    IF v_агентство IS NULL THEN
        RAISE NOTICE 'Нет ни одного коллекторского агентства.';
        RETURN;
    END IF;

    FOR v_должник IN
        SELECT з.ID_Клиента, к.Фамилия || ' ' || к.Имя AS ФИО, з.ID_Займа,
            SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов + п.Остаток_погашения_штрафа) AS сумма_просрочки,
            MIN(п.Плановая_дата_оплаты) AS дата_самой_ранней_просрочки
        FROM План_выплат п
        JOIN Документ_займа з ON п.ID_Займа = з.ID_Займа
        JOIN Клиент к ON з.ID_Клиента = к.ID_Клиента
        WHERE п.Статус_исполнения = 'Просрочено'
        GROUP BY з.ID_Клиента, к.Фамилия, к.Имя, з.ID_Займа
        HAVING SUM(п.Остаток_погашения_основы + п.Остаток_погашения_процентов + п.Остаток_погашения_штрафа) > v_лимит
            OR MIN(п.Плановая_дата_оплаты) < CURRENT_DATE - p_мес * INTERVAL '1 month'
    LOOP
        SELECT COUNT(*) INTO v_существует
        FROM Договор_о_сотрудничестве
        WHERE ID_Займа = v_должник.ID_Займа AND Статус_договора = 'В работе';

        IF v_существует > 0 THEN
            CONTINUE;
        END IF;

        v_причина := '';
        IF v_должник.сумма_просрочки > v_лимит THEN
            v_причина := 'сумма просрочки ' || v_должник.сумма_просрочки || ' руб.';
        END IF;
        IF v_должник.дата_самой_ранней_просрочки < CURRENT_DATE - p_мес * INTERVAL '1 month' THEN
            IF v_причина <> '' THEN v_причина := v_причина || '; '; END IF;
            v_причина := v_причина || 'просрочка более ' || p_мес || ' месяцев';
        END IF;

        v_рег_номер := 'КД-' || v_должник.ID_Займа || '-' || EXTRACT(EPOCH FROM CURRENT_TIMESTAMP)::BIGINT;

        INSERT INTO Договор_о_сотрудничестве
            (Регистрационный_номер, Дата_подписания, Статус_договора, ID_Займа, ID_Агентства, ID_Бухгалтера)
        VALUES (v_рег_номер, CURRENT_DATE, 'В работе', v_должник.ID_Займа, v_агентство,
            (SELECT ID_Бухгалтера FROM Документ_займа WHERE ID_Займа = v_должник.ID_Займа));

        RAISE NOTICE 'Договор создан: клиент %, займ %, причина: %',
            v_должник.ФИО, v_должник.ID_Займа, v_причина;
    END LOOP;
END;
$$;


-- 2
-- Для просрочки 1 день  — создаёт штрафной пункт плана.
-- Для просрочки > 1 дня — пересчитывает сумму штрафа.
CREATE OR REPLACE PROCEDURE proc_проверка_задолженностей()
LANGUAGE plpgsql
AS $$
DECLARE
    v_пункт      RECORD;
    v_просрочка  INT;
    v_штраф      DECIMAL(15,2);
    v_тип_штрафа INT;
    v_платеж RECORD;
    v_неоплаченный_пункт RECORD;
    v_остаток_платежа DECIMAL(15,2);
    v_сумма_пункта DECIMAL(15,2);
BEGIN
    SELECT ID_Типа_начисления INTO v_тип_штрафа
    FROM Тип_начисления WHERE Название ILIKE '%штраф%' LIMIT 1;

    IF v_тип_штрафа IS NULL THEN
        RAISE EXCEPTION 'Не найден тип начисления "Штраф" в таблице Тип_начисления';
    END IF;

    FOR v_платеж IN
        SELECT p.*,
               p.Внесенная_сумма - COALESCE(
                   (SELECT SUM(Остаток_погашения_основы + Остаток_погашения_процентов + Остаток_погашения_штрафа)
                    FROM План_выплат WHERE ID_Платежа = p.ID_Платежа), 0) AS доступный_остаток
        FROM Платёжный_документ p
        WHERE p.Внесенная_сумма > COALESCE(
                   (SELECT SUM(Остаток_погашения_основы + Остаток_погашения_процентов + Остаток_погашения_штрафа)
                    FROM План_выплат WHERE ID_Платежа = p.ID_Платежа), 0)
    LOOP
        v_остаток_платежа := v_платеж.доступный_остаток;
        FOR v_неоплаченный_пункт IN
            SELECT п.*, з.Текущий_статус
            FROM План_выплат п
            JOIN Документ_займа з ON п.ID_Займа = з.ID_Займа
            WHERE з.ID_Клиента = v_платеж.ID_Клиента
              AND п.ID_Платежа IS NULL
              AND п.Статус_исполнения IN ('Ожидает', 'Просрочено')
            ORDER BY п.Плановая_дата_оплаты ASC, п.ID_Плана ASC
        LOOP
            v_сумма_пункта := v_неоплаченный_пункт.Остаток_погашения_основы +
                              v_неоплаченный_пункт.Остаток_погашения_процентов +
                              v_неоплаченный_пункт.Остаток_погашения_штрафа;
            IF v_остаток_платежа >= v_сумма_пункта AND v_сумма_пункта > 0 THEN
                UPDATE План_выплат
                SET ID_Платежа = v_платеж.ID_Платежа
                WHERE ID_Плана = v_неоплаченный_пункт.ID_Плана;
                
                v_остаток_платежа := v_остаток_платежа - v_сумма_пункта;
                RAISE NOTICE 'Пункт плана % оплачен платежом %', v_неоплаченный_пункт.ID_Плана, v_платеж.ID_Платежа;
            END IF;
            
            EXIT WHEN v_остаток_платежа = 0;
        END LOOP;
    END LOOP;

    FOR v_пункт IN
        SELECT з.ID_Займа, 
               з.ID_Бухгалтера,
               з.Пени_процент_день,
               MIN(п.Плановая_дата_оплаты) AS дата_начала_просрочки,
               MAX(CURRENT_DATE - п.Плановая_дата_оплаты) AS макс_просрочка,
               SUM(
                   (п.Остаток_погашения_основы + п.Остаток_погашения_процентов) 
                   * (CURRENT_DATE - п.Плановая_дата_оплаты)
               ) AS база_штрафодней
        FROM План_выплат п
        JOIN Документ_займа з ON п.ID_Займа = з.ID_Займа
        WHERE п.Плановая_дата_оплаты < CURRENT_DATE
          AND п.Статус_исполнения IN ('Ожидает', 'Просрочено')
          AND з.Текущий_статус = 'Активен'
          AND п.ID_Типа_начисления <> v_тип_штрафа
        GROUP BY з.ID_Займа, з.ID_Бухгалтера, з.Пени_процент_день
    LOOP
        v_штраф := ROUND(v_пункт.база_штрафодней * (v_пункт.Пени_процент_день / 100.0), 2);

        -- Устанавливаем статус 'Просрочено' для всех основных пунктов этого займа
        UPDATE План_выплат 
        SET Статус_исполнения = 'Просрочено' 
        WHERE ID_Займа = v_пункт.ID_Займа 
          AND Плановая_дата_оплаты < CURRENT_DATE 
          AND Статус_исполнения = 'Ожидает' 
          AND ID_Типа_начисления <> v_тип_штрафа;

        -- Проверяем наличие уже существующего штрафного пункта
        IF EXISTS (
            SELECT 1 FROM План_выплат 
            WHERE ID_Займа = v_пункт.ID_Займа AND ID_Типа_начисления = v_тип_штрафа
              AND Статус_исполнения IN ('Ожидает', 'Просрочено')
        ) THEN
            -- Штрафной пункт уже существует, просто обновляем его итоговую сумму
            UPDATE План_выплат SET Остаток_погашения_штрафа = v_штраф
            WHERE ID_Займа = v_пункт.ID_Займа AND ID_Типа_начисления = v_тип_штрафа
              AND Статус_исполнения IN ('Ожидает', 'Просрочено');
              
            RAISE NOTICE 'Займ %: макс просрочка % дней, пересчитана общая сумма штрафа % руб.',
                v_пункт.ID_Займа, v_пункт.макс_просрочка, v_штраф;
        ELSE
            -- Создаем новый единый штрафной пункт для всего займа
            INSERT INTO План_выплат
                (Плановая_дата_оплаты, Остаток_погашения_основы, Остаток_погашения_процентов,
                 Остаток_погашения_штрафа, Статус_исполнения, ID_Займа, ID_Бухгалтера, ID_Типа_начисления)
            VALUES (v_пункт.дата_начала_просрочки + 1, 0, 0, v_штраф, 'Ожидает', v_пункт.ID_Займа, v_пункт.ID_Бухгалтера, v_тип_штрафа);

            RAISE NOTICE 'Займ %: первый день просрочки, создан общий штрафной пункт % руб.',
                v_пункт.ID_Займа, v_штраф;
        END IF;
    END LOOP;
END;
$$;